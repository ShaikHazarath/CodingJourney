package demo;

public class Staticvariables {

	public static void main(String[] args) {
		
		int a = 100;
		int b = 200;
		{
			System.out.println(a +b);
		}
		int a1 = 100;
		int b1 = 300;
		{
			System.out.println( a1 -b1);
		}
		int e = 500;
		double c = 20.01;
		{
			System.out.println(e*c);
		}
	}
	

}
