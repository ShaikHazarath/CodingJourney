package demo;
public class Adding {
	static void add()
	{
		int a = 10;
		int b = 20;
		System.out.println(a+b);
	}
	static void sub()
	{
		int c = 20;
		int d = 30;
		System.out.println(c-d);
	}
	static void division()
	{
		int a = 300;
		int b = 20;
		System.out.println(a/b);
	}
	static void multiply()
	{
		int a = 500;
		int b = 20;
		System.out.println(a*b);
	}
	public static void main(String[] args)
	{
     add();
     sub ();
     division();
     multiply();    
	}

}
